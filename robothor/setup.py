def create_workspace(path: Path) -> None:
    """Create workspace directory structure with brain/ scaffold from templates."""
    path.mkdir(parents=True, exist_ok=True)
    (path / "memory").mkdir(exist_ok=True)
    (path / "faces").mkdir(exist_ok=True)

    # Brain directory — identity, runtime state, agent instructions
    brain = path / "brain"
    brain.mkdir(exist_ok=True)
    (brain / "memory").mkdir(exist_ok=True)

    # Docs directories — agent manifests and workflows
    docs = path / "docs"
    (docs / "agents").mkdir(parents=True, exist_ok=True)
    (docs / "workflows").mkdir(parents=True, exist_ok=True)

    # Copy template files into brain/ (never overwrite existing)
    template_dir = _find_template_dir()
    if template_dir:
        brain_templates = [
            "SOUL.md",
            "IDENTITY.md",
            "AGENTS.md",
            "TOOLS.md",
            "HEARTBEAT.md",
            "USER.md",
            "BOOTSTRAP.md",
        ]
        for filename in brain_templates:
            src = template_dir / filename
            dst = brain / filename
            if src.exists() and not dst.exists():
                dst.write_text(src.read_text())

        # Copy agent manifest template to docs/agents/
        for name in ("agent-manifest.yaml", "agent-instructions.md"):
            src = template_dir / name
            dst = docs / "agents" / name
            if src.exists() and not dst.exists():
                dst.write_text(src.read_text())

        # Copy CLAUDE.md files to workspace root
        for fname in ("CLAUDE.md", "AGENT_BUILDER.md", "ONBOARDING.md"):
            src = template_dir / fname
            dst = path / fname
            if src.exists() and not dst.exists():
                dst.write_text(src.read_text())


def _find_template_dir() -> Path | None:
    """Locate the templates/ directory (bundled or in-repo)."""
    # Bundled in wheel
    bundled = Path(__file__).parent / "templates"
    if bundled.is_dir():
        return bundled
    # Development: repo root
    repo_root = Path(__file__).parent.parent
    dev_path = repo_root / "templates"
    if dev_path.is_dir():
        return dev_path
    return None


def run_migration(db_config: DatabaseConfig) -> int:
    """Run database migration. Returns table count on success, -1 on failure."""
    from robothor.cli import _find_migration_sql

    sql = _find_migration_sql()
    if sql is None:
        return -1

    try:
        import psycopg2

        conn = psycopg2.connect(**db_config.dict, connect_timeout=5)
        conn.autocommit = True
        with conn.cursor() as cur:
            cur.execute(sql)
            # Count tables
            cur.execute(
                "SELECT count(*) FROM information_schema.tables WHERE table_schema = 'public'"
            )
            row = cur.fetchone()
            count: int = row[0] if row else 0
        conn.close()
        return count
    except Exception:
        return -1


def pull_ollama_models(base_url: str, models: list[str]) -> None:
    """Pull required Ollama models with progress output."""
    for model in models:
        print(f"  Pulling {model} ...", end=" ", flush=True)
        try:
            with httpx.stream(
                "POST",
                f"{base_url}/api/pull",
                json={"name": model},
                timeout=httpx.Timeout(5.0, read=600.0),
            ) as resp:
                resp.raise_for_status()
                last_status = ""
                for line in resp.iter_lines():
                    if not line:
                        continue
                    try:
                        data = json.loads(line)
                    except json.JSONDecodeError:
                        continue
                    status = data.get("status", "")
                    if status != last_status:
                        last_status = status
                print("done")
        except Exception as e:
            print(f"failed ({e})")


def write_env_file(
    path: Path,
    db: DatabaseConfig,
    redis: RedisConfig,
    ollama: OllamaConfig,
    *,
    owner_name: str = "",
    ai_name: str = "Robothor",
    owner_email: str = "",
    ai_email: str = "",
    timezone: str = "America/New_York",
    profiles: list[str] | None = None,
    yes: bool = False,
) -> bool:
    """Write .env file with configuration. Returns True if written."""
    if path.exists() and not yes:
        overwrite = input(f"  {path} exists. Overwrite? [y/N]: ").strip().lower()
        if overwrite != "y":
            print(f"  Keeping existing {path}")
            return False

    # Secrets are NEVER written to .env — use SOPS or environment variables
    lines = [
        f"ROBOTHOR_OWNER_NAME={owner_name}",
        f"ROBOTHOR_AI_NAME={ai_name}",
        f"ROBOTHOR_OWNER_EMAIL={owner_email}",
        f"ROBOTHOR_AI_EMAIL={ai_email}",
        f"ROBOTHOR_TIMEZONE={timezone}",
        f"ROBOTHOR_DB_HOST={db.host}",
        f"ROBOTHOR_DB_PORT={db.port}",
        f"ROBOTHOR_DB_NAME={db.name}",
        f"ROBOTHOR_DB_USER={db.user}",
        "# ROBOTHOR_DB_PASSWORD — set via SOPS secrets or env, not stored here",
        "ROBOTHOR_DB_PASSWORD=",
        f"ROBOTHOR_REDIS_HOST={redis.host}",
        f"ROBOTHOR_REDIS_PORT={redis.port}",
        f"ROBOTHOR_OLLAMA_HOST={ollama.host}",
        f"ROBOTHOR_OLLAMA_PORT={ollama.port}",
        f"ROBOTHOR_WORKSPACE={path.parent}",
    ]
    if profiles:
        lines.append(f"COMPOSE_PROFILES={','.join(profiles)}")
    path.write_text("\n".join(lines) + "\n")
    return True


def generate_docker_compose(workspace: Path, db_password: str) -> Path:
    """Generate a docker-compose.yml in the workspace directory."""
    compose_path = workspace / "docker-compose.yml"
    content = DOCKER_COMPOSE_TEMPLATE.format(db_password=db_password)
    compose_path.write_text(content)
    return compose_path


def wait_for_services(workspace: Path, timeout: int = 60) -> bool:
    """Start Docker containers and wait for health checks."""
    compose_file = workspace / "docker-compose.yml"
    if not compose_file.exists():
        return False

    result = subprocess.run(
        ["docker", "compose", "-f", str(compose_file), "up", "-d"],
        capture_output=True,
        text=True,
        cwd=str(workspace),
    )
    if result.returncode != 0:
        print(f"\n  Docker error: {result.stderr.strip()}")
        return False

    # Poll for PG readiness
    deadline = time.monotonic() + timeout
    while time.monotonic() < deadline:
        try:
            import psycopg2

            conn = psycopg2.connect(
                host="127.0.0.1",
                port=5432,
                dbname="robothor_memory",
                user="robothor",
                password="robothor",
                connect_timeout=2,
            )
            conn.close()
            return True
        except Exception:
            time.sleep(2)

    return False


