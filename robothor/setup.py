def _save_init_state(workspace: Path, step: str, status: str) -> None:
    """Write init progress to .robothor/init_state.yaml for resume support."""
    state_dir = workspace / ".robothor"
    state_dir.mkdir(parents=True, exist_ok=True)
    state_path = state_dir / "init_state.yaml"

    state: dict[str, Any] = {}
    if state_path.exists():
        state = yaml.safe_load(state_path.read_text()) or {}
    state[step] = status

    state_path.write_text(yaml.dump(state, default_flow_style=False))


def _load_init_state(workspace: Path) -> dict[str, str]:
    """Load init progress from .robothor/init_state.yaml."""
    state_path = workspace / ".robothor" / "init_state.yaml"
    if not state_path.exists():
        return {}
    return yaml.safe_load(state_path.read_text()) or {}


def run_init(args: Any) -> int:
    """Main orchestrator for the init wizard."""
    print()
    print("  Genus OS Setup")
    print("  " + "=" * 14)
    print()

    # 1. Check prerequisites
    print("  Checking prerequisites...")
    prereqs = check_prerequisites(
        docker_required=getattr(args, "docker", False),
    )
    _print_prerequisites(prereqs)
    print()

    # Bail on missing required prerequisites
    missing_required = [p for p in prereqs if p["required"] and not p["found"]]
    if missing_required:
        print("  Cannot continue — install the required tools above.")
        return 1

    # 2. Identity
    workspace = Path(getattr(args, "workspace", None) or Path.home() / "robothor")
    yes = getattr(args, "yes", False)
    docker = getattr(args, "docker", False)
    skip_models = getattr(args, "skip_models", False)
    skip_db = getattr(args, "skip_db", False)

    # Check for existing installation
    env_exists = (workspace / ".env").exists()
    config_exists = (workspace / ".robothor" / "config.yaml").exists()
    if env_exists or config_exists:
        print("  Existing installation detected.")
        if not yes:
            choice = input("  [U]pgrade existing / [F]resh install / [Q]uit? [U]: ").strip().lower()
            if choice == "q":
                return 0
            if choice == "f":
                pass  # Continue with full setup
            else:
                # Upgrade mode: load init_state and skip completed steps
                pass
        # In --yes mode, default to upgrade behavior

    if yes:
        ai_name = os.environ.get("ROBOTHOR_AI_NAME", "Robothor")
        owner_name = os.environ.get("ROBOTHOR_OWNER_NAME", "")
        owner_email = os.environ.get("ROBOTHOR_OWNER_EMAIL", "")
        ai_email = os.environ.get("ROBOTHOR_AI_EMAIL", "")
        timezone = os.environ.get("ROBOTHOR_TIMEZONE", "America/New_York")
    else:
        print("  Identity:")
        ai_name = input("    What should your AI be called? [Robothor]: ").strip() or "Robothor"
        owner_name = input("    Your name: ").strip()
        owner_email = input("    Your email: ").strip()
        ai_email = input(f"    {ai_name}'s email (leave blank to skip): ").strip()
        timezone = input("    Your timezone [America/New_York]: ").strip() or "America/New_York"
        print()

    # 3. Docker mode: generate compose and start containers
    db_password = ""
    if docker:
        db_password = os.environ.get("ROBOTHOR_DB_PASSWORD", "") or secrets.token_urlsafe(16)
        if not yes:
            pw = getpass.getpass(f"  Docker DB password [{db_password}]: ")
            if pw:
                db_password = pw

    # 4. Prompt for DB config (interactive only, non-Docker)
    if docker:
        db_config = DatabaseConfig(
            host="127.0.0.1",
            port=5432,
            name="robothor_memory",
            user="robothor",
            password=db_password,
        )
    elif yes:
        db_config = _db_config_from_env()
    else:
        db_config = prompt_db_config()

    redis_config = _redis_config_from_env()
    ollama_config = _ollama_config_from_env()

    # Load init state for resume support
    init_state = _load_init_state(workspace)

    # 5. Create workspace (skip if completed)
    if init_state.get("workspace") != "completed":
        print(f"  Creating workspace {workspace} ...", end=" ", flush=True)
        create_workspace(workspace)
        print("done")
        _save_init_state(workspace, "workspace", "completed")
    else:
        print(f"  Creating workspace {workspace} ... skipped (exists)")

    # 6. Docker mode: generate compose, start, wait
    if docker:
        if init_state.get("docker") != "completed":
            compose_path = generate_docker_compose(workspace, db_password)
            print(f"  Generated {compose_path}")
            print("  Starting Docker containers ...", end=" ", flush=True)
            if wait_for_services(workspace, timeout=90):
                print("done")
            else:
                print("timed out (containers may still be starting)")
            _save_init_state(workspace, "docker", "completed")
        else:
            print("  Docker containers ... skipped (exists)")

    # 7. Run migration
    if not skip_db:
        if init_state.get("migration") != "completed":
            print("  Running migration ...", end=" ", flush=True)
            table_count = run_migration(db_config)
            if table_count >= 0:
                print(f"{table_count} tables created")
                _save_init_state(workspace, "migration", "completed")
            else:
                print("FAILED")
                print("  Error: Database migration failed. Check connection settings:")
                print(f"    Host: {db_config.host}:{db_config.port}")
                print(f"    Database: {db_config.name}")
                print(f"    User: {db_config.user}")
                return 1
        else:
            print("  Running migration ... skipped (completed)")

        # Validate database connection
        if init_state.get("db_validate") != "completed":
            print("  Validating database ...", end=" ", flush=True)
            try:
                import psycopg2

                conn = psycopg2.connect(**db_config.dict, connect_timeout=5)
                with conn.cursor() as cur:
                    cur.execute("SELECT 1")
                conn.close()
                print("connected")
                _save_init_state(workspace, "db_validate", "completed")
            except Exception as e:
                print(f"FAILED ({e})")
                return 1
        else:
            print("  Validating database ... skipped (verified)")

    # 8. Pull Ollama models
    if not skip_models:
        if init_state.get("models") != "completed":
            pull_ollama_models(ollama_config.base_url, REQUIRED_MODELS)
            _save_init_state(workspace, "models", "completed")
        else:
            print("  Pulling models ... skipped (completed)")

    # 9. Subsystem selection
    profiles: list[str] = []
    if not yes:
        profiles = prompt_subsystems()
    print()

    # 10. Generate vault master key
    if init_state.get("vault") != "completed":
        print("  Generating vault master key ...", end=" ", flush=True)
        setup_vault_key(workspace)
        print("done")
        _save_init_state(workspace, "vault", "completed")
    else:
        print("  Generating vault master key ... skipped (exists)")

    # 11. Write env file
    if init_state.get("env_file") != "completed":
        env_path = workspace / ".env"
        wrote = write_env_file(
            env_path,
            db_config,
            redis_config,
            ollama_config,
            owner_name=owner_name,
            ai_name=ai_name,
            owner_email=owner_email,
            ai_email=ai_email,
            timezone=timezone,
            profiles=profiles,
            yes=yes,
        )
        if wrote:
            print(f"  Saving config to {env_path} ... done")
            _save_init_state(workspace, "env_file", "completed")
    else:
        env_path = workspace / ".env"
        print(f"  Saving config to {env_path} ... skipped (exists)")

    # 12. Resolve template variables in CLAUDE.md files
    for fname in ("CLAUDE.md", "AGENT_BUILDER.md"):
        md_path = workspace / fname
        if md_path.exists():
            content = md_path.read_text()
            content = content.replace("{{ai_name}}", ai_name)
            content = content.replace("{{owner_name}}", owner_name)
            md_path.write_text(content)

    # 13. Agent template setup (if templates exist)
    if not yes:
        _offer_agent_setup(workspace)

    # Summary
    print()
    print("  \u2713 Genus OS initialized")
    print(f"    AI Name:     {ai_name}")
    print(f"    Workspace:   {workspace}")
    print(f"    Database:    postgresql://{db_config.host}:{db_config.port}/{db_config.name}")

    # Show installed models
    model_names = ", ".join(REQUIRED_MODELS) if not skip_models else "skipped"
    print(f"    Models:      {model_names}")

    # Count installed agents
    agent_dir = workspace / "docs" / "agents"
    agent_count = len(list(agent_dir.glob("*.yaml"))) if agent_dir.exists() else 0
    print(f"    Agents:      {agent_count} installed", end="")
    if agent_count == 0:
        print(" (run: robothor agent install --preset standard)", end="")
    print()

    # Engine status
    print("    Engine:      not running (run: sudo systemctl start robothor-engine)")

    print()
    print("  Next steps:")
    print("    1. robothor agent install --preset standard")
    print("    2. sudo systemctl start robothor-engine")
    print("    3. Open Claude Code for identity setup")
    print()
    return 0


def check_prerequisites(*, docker_required: bool = False) -> list[dict[str, Any]]:
    """Check for required and optional tools.

    Returns a list of dicts: {name, found, detail, required, hint}.
    """
    results = []

    # Python (always present)
    results.append(
        {
            "name": "Python",
            "found": True,
            "detail": f"{sys.version.split()[0]}",
            "required": True,
            "hint": "",
        }
    )

    # psql
    psql = shutil.which("psql")
    results.append(
        {
            "name": "PostgreSQL (psql)",
            "found": psql is not None,
            "detail": "found" if psql else "not found",
            "required": False,
            "hint": _install_hint("postgresql-client", "postgresql"),
        }
    )

    # redis-cli
    redis_cli = shutil.which("redis-cli")
    results.append(
        {
            "name": "Redis (redis-cli)",
            "found": redis_cli is not None,
            "detail": "found" if redis_cli else "not found",
            "required": False,
            "hint": _install_hint("redis-server", "redis"),
        }
    )

    # Ollama
    ollama_found = False
    ollama_detail = "not reachable"
    try:
        base_url = os.environ.get("ROBOTHOR_OLLAMA_HOST", "127.0.0.1")
        port = os.environ.get("ROBOTHOR_OLLAMA_PORT", "11434")
        resp = httpx.get(f"http://{base_url}:{port}/api/tags", timeout=3)
        resp.raise_for_status()
        models = resp.json().get("models", [])
        ollama_found = True
        ollama_detail = f"{len(models)} model(s) loaded"
    except Exception:
        ollama_detail = "not reachable"
    results.append(
        {
            "name": "Ollama",
            "found": ollama_found,
            "detail": ollama_detail,
            "required": False,
            "hint": "curl -fsSL https://ollama.com/install.sh | sh",
        }
    )

    # Docker (required only with --docker)
    docker_bin = shutil.which("docker")
    results.append(
        {
            "name": "Docker",
            "found": docker_bin is not None,
            "detail": "found" if docker_bin else "not found",
            "required": docker_required,
            "hint": "https://docs.docker.com/engine/install/",
        }
    )

    return results


def prompt_db_config() -> DatabaseConfig:
    """Interactive prompts for database configuration."""
    defaults = _db_config_from_env()
    print("  Database configuration:")

    host = input(f"    Host [{defaults.host}]: ").strip() or defaults.host
    port_str = input(f"    Port [{defaults.port}]: ").strip()
    port = int(port_str) if port_str else defaults.port
    name = input(f"    Database [{defaults.name}]: ").strip() or defaults.name
    user = input(f"    User [{defaults.user}]: ").strip() or defaults.user
    password = getpass.getpass("    Password: ") if not defaults.password else defaults.password

    print()
    return DatabaseConfig(host=host, port=port, name=name, user=user, password=password)


def prompt_subsystems() -> list[str]:
    """Interactive multi-select for optional Docker Compose profiles."""
    options = [
        ("monitoring", "Monitoring (Uptime Kuma)", "web dashboard for service health"),
        ("tts", "Voice synthesis (Kokoro TTS)", "local text-to-speech"),
        ("media", "Webcam/vision (MediaMTX)", "RTSP/HLS camera streaming"),
        ("tunnel", "Reverse proxy (Cloudflare tunnel)", "secure external access"),
    ]
    print("  Subsystems (optional Docker services):")
    for i, (_, label, desc) in enumerate(options, 1):
        print(f"    {i}. {label} — {desc}")
    selection = input("  Enable (comma-separated numbers, or Enter to skip): ").strip()
    if not selection:
        return []
    profiles = []
    for part in selection.split(","):
        part = part.strip()
        if part.isdigit():
            idx = int(part) - 1
            if 0 <= idx < len(options):
                profiles.append(options[idx][0])
    if profiles:
        print(f"  Enabled: {', '.join(profiles)}")
    return profiles


